public class Assignment13 {

    public static void main(String[] args) {
        
    //13. Java program to display characters from A to Z using loop. Print English alphabets using for 
    //loop in Java. learn to print only uppercased and lowercased alphabets.

        char c;

            //Print only uppercased alphabets.
            for(c = 'A'; c <= 'Z'; ++c){
                System.out.print(c + " " );
            }
        
            //Print only lowercased alphabets.
            for(c = 'a'; c <= 'z'; ++c){
                  System.out.print(c + " ");
            } 
   
}
    
}
